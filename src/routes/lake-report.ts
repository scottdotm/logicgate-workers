import type { Env } from '../index';

type Inquiry = {
  id: number;
  name: string;
  email: string;
  organization: string;
  phone: string | null;
  part_107: string | null;
  existing_clients: string | null;
  message: string | null;
  interest: string;
  status: string;
  created_at: string;
};

type Upload = {
  id: number;
  inquiry_id: number;
  filename: string;
  content_type: string;
  r2_key: string;
  lake_name: string | null;
  file_size_bytes: number;
  uploaded_at: string;
};

