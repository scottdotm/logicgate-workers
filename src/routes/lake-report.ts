function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function formatBytes(bytes: number): string {
  if (bytes === 0) return '0 B';
  const k = 1024;
  const sizes = ['B', 'KB', 'MB', 'GB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return `${parseFloat((bytes / Math.pow(k, i)).toFixed(1))} ${sizes[i]}`;
}

export async function fetchInquiryAndUploads(
  env: Env,
  inquiryId: number
): Promise<{ inquiry: Inquiry | null; uploads: Upload[] }> {
  const inquiry = await env.DB.prepare(
    'SELECT * FROM inquiries WHERE id = ?'
  ).bind(inquiryId).first<Inquiry>();

  if (!inquiry) {
    return { inquiry: null, uploads: [] };
  }

  const result = await env.DB.prepare(
    'SELECT * FROM uploads WHERE inquiry_id = ? ORDER BY uploaded_at ASC'
  ).bind(inquiryId).all<Upload>();

  return { inquiry, uploads: result.results || [] };
}
